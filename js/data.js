function displayInfo(nivel, materia, open) {
    var popup = document.getElementById("popup");
    var image = document.getElementById("imagen-materia");
    var title = document.getElementById("title-materia");
    var text = document.getElementById("text-materia");
    let result;
    if (open) {
        result = info.nivelesEducativos[nivel].oferta.filter(search => {
            return search.name === materia;
        });
        image.src = result[0].img;
        title.innerHTML = result[0].name;
        text.innerHTML = result[0].content;
        popup.style.visibility = "visible";
        popup.style.opacity = "1";
    } else {
        popup.style.opacity = "0";
        popup.style.visibility = "hidden";
    }
}